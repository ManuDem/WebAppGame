export function sanitizeLanguage(raw?: string | null): SupportedLanguage {
    return raw === 'en' ? 'en' : DEFAULT_LANGUAGE;
}

export function t(
    language: SupportedLanguage,
    key: string,
    vars?: Record<string, string | number>,
): string {
    const fallback = translations[DEFAULT_LANGUAGE][key] ?? key;
    const template = translations[language]?.[key] ?? fallback;

    if (!vars) return template;

    return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (_all, token) => {
        const value = vars[token];
        return value === undefined || value === null ? '' : String(value);
    });
}
